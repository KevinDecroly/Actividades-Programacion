
// Definición de un enumerado (enum) llamado Genero
// Un enum permite definir un conjunto fijo de constantes con nombre
public enum Genero {

    // =========================================================================
    // Constantes del enum: representan los diferentes géneros de película
    // =========================================================================

    ACCION,     // Género de acción
    AVENTURA,   // Género de aventura
    FANTASIA,   // Género de fantasía
    SCIFI,      // Ciencia ficción
    TERROR,     // Género de terror
    DRAMA,      // Género dramático
    COMEDIA     // Género de comedia

    // =========================================================================
    // Nota:
    // Un enum es útil porque asegura que solo se puedan usar estos valores
    // No se pueden crear valores que no estén definidos aquí.
    // Se puede usar con las clases Pelicula o VideoDaw para asignar géneros.
    // =========================================================================
}